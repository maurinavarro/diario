package com.secc_sistema.admision_cupo.Model_Entities;


public class EstadoFormulario {

    private Long idDetenido;

    private String nombreDetenido;

    private String estadoDetenido;

    private String estadoDatosJudiciales;

    private String estadoDatosCriminologicos;

    public Long getIdDetenido() {
        return idDetenido;
    }
    public void setIdDetenido(Long idDetenido) {
        this.idDetenido = idDetenido;
    }
    public String getNombreDetenido() {
        return nombreDetenido;
    }
    public void setNombreDetenido(String nombreDetenido) {
        this.nombreDetenido = nombreDetenido;
    }
    public String getEstadoDetenido() {
        return estadoDetenido;
    }
    public void setEstadoDetenido(String estadoDetenido) {
        this.estadoDetenido = estadoDetenido;
    }
    public String getEstadoDatosJudiciales() {
        return estadoDatosJudiciales;
    }
    public void setEstadoDatosJudiciales(String estadoDatosJudiciales) {
        this.estadoDatosJudiciales = estadoDatosJudiciales;
    }
    public String getEstadoDatosCriminologicos() {
        return estadoDatosCriminologicos;
    }
    public void setEstadoDatosCriminologicos(String estadoDatosCriminologicos) {
        this.estadoDatosCriminologicos = estadoDatosCriminologicos;
    }

    
    

    

}
